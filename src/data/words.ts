export function getRandomWord(category?: string): WordPair {
  let filteredWords = wordList

  if (category && category !== 'Todas') {
    filteredWords = wordList.filter(word => word.category === category)
  }

  const randomIndex = Math.floor(Math.random() * filteredWords.length)
  return filteredWords[randomIndex]
}

export function getAllCategories(): string[] {
  const categories = Array.from(new Set(wordList.map(word => word.category)))
  return ['Todas', ...categories.sort()]
}
